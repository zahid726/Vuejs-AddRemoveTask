<template>
<div @dblclick="$emit('toggle-reminder', task.id)" :class="[task.reminder ? 'reminder': '','task' ]">
<h3>{{task.text}}
    <i @click="delete_task(task.id)" class="fas fa-times"></i>
</h3>
<p>{{task.day}}</p>
</div> 
    
</template>

<script>
export default {
    name: 'Task',
    props:{
        task: Object,
    },
    methods:{
        delete_task(id){
          this.$emit('delete-task', id)
        }
    }

}
</script>
